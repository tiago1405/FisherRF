# from https://blog.lostinmyterminal.com/python/2015/05/12/random-rotation-matrix.html
def rand_rotation_matrix(deflection=1.0, randnums=None):
    """
    Creates a random rotation matrix.
    
    deflection: the magnitude of the rotation. For 0, no rotation; for 1, competely random
    rotation. Small deflection => small perturbation.
    randnums: 3 random numbers in the range [0, 1]. If `None`, they will be auto-generated.
    """
    # from http://www.realtimerendering.com/resources/GraphicsGems/gemsiii/rand_rotation.c
    
    if randnums is None:
        randnums = np.random.uniform(size=(3,))
        
    theta, phi, z = randnums
    
    theta = theta * 2.0*deflection*np.pi  # Rotation about the pole (Z).
    phi = phi * 2.0*np.pi  # For direction of pole deflection.
    z = z * 2.0*deflection  # For magnitude of pole deflection.
    
    # Compute a vector V used for distributing points over the sphere
    # via the reflection I - V Transpose(V).  This formulation of V
    # will guarantee that if x[1] and x[2] are uniformly distributed,
    # the reflected points will be uniform on the sphere.  Note that V
    # has length sqrt(2) to eliminate the 2 in the Householder matrix.
    
    r = np.sqrt(z)
    Vx, Vy, Vz = V = (
        np.sin(phi) * r,
        np.cos(phi) * r,
        np.sqrt(2.0 - z)
        )
    
    st = np.sin(theta)
    ct = np.cos(theta)
    
    R = np.array(((ct, st, 0), (-st, ct, 0), (0, 0, 1)))
    
    # Construct the rotation matrix  ( V Transpose(V) - I ) R.
    
    M = (np.outer(V, V) - np.eye(3)).dot(R)
    return M


def dump_cam_info(uid, R_perturb, T_perturbed, FoVx, FoVy, img_rel_path, image_name=""):
    return {
        "uid": uid, 
        "R": R_perturb.tolist(),
        "T": T_perturbed.tolist(),
        "FoVx": float(FoVx),
        "FoVy": float(FoVy),
        "img_path": img_rel_path,
        "image_name": image_name,
    }

def load_cam_info(info_dict, base_path, device="cuda"):
    img = Image.open(f"{base_path}/{info_dict['img_path']}")
    th_img = torch.from_numpy(np.array(img)) / 255.0
    if len(th_img.shape) == 3:
        th_img = th_img.permute(2, 0, 1)
    else:
        th_img =  th_img.unsqueeze(dim=-1).permute(2, 0, 1)

    return Camera(info_dict["uid"], np.array(info_dict["R"]), np.array(info_dict["T"]), FoVx=info_dict["FoVx"], FoVy=info_dict["FoVy"],
                  image=th_img, gt_alpha_mask=None, uid=info_dict["uid"], data_device=device, image_name=info_dict["image_name"])
    